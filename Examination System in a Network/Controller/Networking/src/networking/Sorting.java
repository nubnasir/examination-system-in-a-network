    private void sortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sortActionPerformed
        // TODO add your handling code here:
        if (details.getText() != null && !"".equals(details.getText())) {
            try {
                se = new SortingEngine(this);
                se.openFile();
                se.fin.format("%s", details.getText());
                se.closeFile();

                se.openFloat();
                prog.setMaximum(se.getLen() * 3);
                prog.setValue(0);
                se.openLine();
                se.marksSort();
                se.checkAndWrite();
                se.writeResult();
                prog.setMaximum(100);
                prog.setValue(prog.getMaximum());
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Fromating problem or something error: " + ex);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Nothing found in screen.");
        }

    }//GEN-LAST:event_sortActionPerformed

