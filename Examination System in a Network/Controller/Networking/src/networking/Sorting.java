    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            JFileChooser fc = new JFileChooser();
            fc.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
            int result = fc.showOpenDialog(this);
            if (result == JFileChooser.CANCEL_OPTION) {
            }
            File fileName = fc.getSelectedFile();
            if (fileName.exists()) {
                fileArea.setText(fileArea.getText() + fileName.getPath() + "\n");
                Scanner input = new Scanner(new File(fileName.getPath()));
                while (input.hasNext()) {
                    details.setText(details.getText() + input.nextLine() + "\n");
                }
                input.close();
            }
        } catch (Exception ex) {
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        details.setText("");
        if (!"".equals(fileArea.getText()) && fileArea.getText() != null) {
            try {
                Formatter fw = new Formatter(new File("D://Exam Controller/file.doc"));
                fw.format("%s", fileArea.getText());
                fw.close();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Error to create file.doc file. " + ex);
            }
            try {
                Scanner input = new Scanner(new File("D://Exam Controller/file.doc"));
                while (input.hasNext()) {
                    Scanner in = new Scanner(new File(input.nextLine()));
                    while (in.hasNext()) {
                        details.setText(details.getText() + in.nextLine() + "\n");
                    }
                    in.close();
                }
                input.close();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Eroor to open file.doc file. " + ex);
            }
            File ff = new File("D://Exam Controller/file.doc");
            ff.delete();
        } else {
            JOptionPane.showMessageDialog(null, "Nothing found...");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

